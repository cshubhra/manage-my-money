package com.finance.application.model;

/**
 * Enum representing different report view types.
 * This corresponds to the original 'report_view_type' in the Ruby on Rails application.
 */
public enum ReportViewType {
    /**
     * Text view type displays the report as text.
     */
    TEXT,
    
    /**
     * Graph view type displays the report as a graph.
     */
    GRAPH
}